const CipherExceptions = [ 'FrancisBacon' ];

let CipherHandler = function (cipher) {
    this.cipherName = cipher;
    this.setCipher(this.cipherName);
};

CipherHandler.prototype.setCipher = function (cipher) {
    this.cipherName = cipher;
    this.cipher = Ciphers[cipher];
}

CipherHandler.prototype.getHtmlForCell = function(name, value) {
    return '<td>' + '<el class="cipher-letter">' + name + '</el><br><el class="cipher-value">' + value + '</el></td>';
}

CipherHandler.prototype.setCipherDisplay = function () {
    if (undefined === this.cipher) return false;

    // Add the header
    var html = '<thead class="thead-dark"><tr><th scope="col" class="thead-dark" colspan="13">';
    html += this.cipherName.replace(/([A-Z])/g, ' $1').trim();
    html +='</th></tr>';
    html += '</thead>';

    var count = 1;

    for (var letter in this.cipher) {
        if (this.cipher.hasOwnProperty(letter)) {
            if (count === 10 || count === 24 || count === 37 || count === 50 ) {
                html += '</tr><tr>';
            }
            if ('0123456789'.indexOf(letter) === -1 ) html += this.getHtmlForCell(letter, this.cipher[letter]);
        }
        count ++;
    }
    $('#cipher_full_display').html(html);
    $('#activeCipher').html($('#ciph option:selected').text());
};

CipherHandler.prototype.getCipherLength = function () {
    if (undefined === this.cipher) return false;

    var count = 0;

    for (letter in this.cipher) {
        if (this.cipher.hasOwnProperty(letter)) {
            count++;
        }
    }

    return count;
}

